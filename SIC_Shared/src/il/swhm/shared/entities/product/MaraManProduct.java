package il.swhm.shared.entities.product;

public class MaraManProduct {
	private String manProductId;
	private String productId;
	private String manufactur;
	private String custProductId;
	
	public String getManProductId() {
		return manProductId;
	}
	public void setManProductId(String manProductId) {
		this.manProductId = manProductId;
	}
	public String getProductId() {
		return productId;
	}
	public void setProductId(String productId) {
		this.productId = productId;
	}
	public String getManufactur() {
		return manufactur;
	}
	public void setManufactur(String manufactur) {
		this.manufactur = manufactur;
	}
	public String getCustProductId() {
		return custProductId;
	}
	public void setCustProductId(String custProductId) {
		this.custProductId = custProductId;
	}
	
	
	
	
	
	
}
